package taoqz.top.策略模式;

/**
 * 用户类
 *
 * @Author taoqingzhou
 **/
public class User {

    /**
     * 年龄
     */
    private Integer age;

    /**
     * 年龄段标签
     */
    private String tag;

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }
}
